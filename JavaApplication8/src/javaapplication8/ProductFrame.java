    private void refreshdata(){
            List<Products> productlist = database.getProducts();
            DefaultTableModel model = (DefaultTableModel)tblProducts.getModel();
            model.setRowCount(0);
            //Iterating through the list
            for(int i = 0; i < productlist.size(); i++){
                    int id = productlist.get(i).ID;
                    String name = productlist.get(i).Name;
                    String price = Integer.toString(productlist.get(i).Price);
                    String description = productlist.get(i).Description;
                    String producttype = productlist.get(i).ProductTypeID;
                    int stock = productlist.get(i).ProductStock;
                    String branchName = productlist.get(i).BranchName;
                    model.addRow(new Object[] {id, name, price, description, producttype, stock, branchName});
                
            }
    }
