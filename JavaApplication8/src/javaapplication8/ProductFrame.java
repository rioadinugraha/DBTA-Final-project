    private void btnDeleteProductsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteProductsActionPerformed
        // TODO add your handling code here:
        //If there is nothing selected, there will be error message
            if(tblProducts.getSelectedRow()==-1){
                JOptionPane.showMessageDialog(null, "Please choose id of the data that want to be deleted", "Delete Action Denied", JOptionPane.ERROR_MESSAGE);
                return;
            }
            //Deleting the id and refreshes the data again
            int row = tblProducts.getSelectedRow();
            int column = tblProducts.getSelectedColumn();
            String id = tblProducts.getValueAt(row, column).toString();
            int idint = Integer.parseInt(id);
            database.deleteProducts(idint);
            refreshdata();
    }//GEN-LAST:event_btnDeleteProductsActionPerformed

