    private void btnPrintBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintBillActionPerformed
        // TODO add your handling code here:
        
        database = new Database();
        try {
            var total = 0;
            for(int i = 0; i < listItems.size();i++){
                total += Integer.parseInt(listItems.get(i).price) * listItems.get(i).qty;
            }
            
            String query = "insert into Transaction (DateTime,EmployeeID, PaymentTypeID, Total, BranchID, ReferenceNo, TaxID)";
            query +=  "values";
            query += " (now(), 1, 1,"+ total +", 1, 'aa', 1)";
            
            
            
            PreparedStatement preparedStatement = database.conn.prepareStatement(query);
          
            preparedStatement.execute();
            
            String query2 = "SELECT ID FROM Transaction order by ID desc limit 1";
            preparedStatement = database.conn.prepareStatement(query2);
            ResultSet rs = preparedStatement.executeQuery();

            var lastId = 1;
            while(rs.next()){
                lastId = rs.getInt("ID");
                break;
            }
            
            
            query = "insert into TransactionDetail (TransactionID, ProductID, QTY) ";
            query +=  "values ";
            
            for(int i = 0 ;i < listItems.size(); i++){
                 if(i != listItems.size() - 1){
                     query += " ("+lastId+","+listItems.get(i).ProductId+","+listItems.get(i).qty+"),";
                 }
                 else{
                     query += " ("+lastId+","+listItems.get(i).ProductId +","+listItems.get(i).qty+")";
                 }
            }
           
            preparedStatement = database.conn.prepareStatement(query);
            preparedStatement.execute();
            
            new BillingResult(lastId).setVisible(true);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnPrintBillActionPerformed

