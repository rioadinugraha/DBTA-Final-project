    private void btnAddProductsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductsActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel)tblProducts.getModel();
        int qty = Integer.parseInt(txtQty.getText());
        String product = cmbProduct.getSelectedItem().toString();
        List <String> listprice = database.getProductPrice();
        int productindex = cmbProduct.getSelectedIndex();
        String price = listprice.get(productindex);
        model.addRow(new Object[] {qty, product, price});
        listQty.add(qty);
        listItems.add(new Items(qty, product, price, listProductsId.get(productindex)));
    }//GEN-LAST:event_btnAddProductsActionPerformed

